#include <stdio.h>
#include <stdlib.h>

int main()
{
   printf("%d\n",5 + 6);
   printf("%d\n", 5 - 6);
   printf("%d\n",5 * 6 );
   printf("%d\n", 6 / 4 );
   printf("%d\n",5 / 6 );
   printf("%d\n\n", 5 % 6 );

   printf("%f\n", 5.0 + 6 );
   printf("%f\n", 5 - 6.0 );
   printf("%f\n", 5.0 * 6.0 );
   printf("%f\n", 6 / 4.0 );
   printf("%.2f\n",6.0 / 4 );

    return 0;
}
